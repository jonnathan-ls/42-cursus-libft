int	ft_isalnum(int c)
{
	int	isalpha;
	int	isdigit;

	isalpha = ((c >= 65 && c <= 90) || (c >= 97 && c <= 122));
	isdigit = (c >= 48 && c <= 57);
	return (isalpha || isdigit);
}
